    private void preencherDados(){
        Produto selecionado = principal.getProduto_selecionado();
        
        if(selecionado == null){
            labelProduto.setText(MensagensUtil.getString(MensagensUtil.MSG_TITLE_PRODUTO_NOVO));
        }else{
            labelProduto.setText(MensagensUtil.getString(MensagensUtil.MSG_TITLE_PRODUTO_EDITAR));
            textNome.setText(selecionado.getNome());
            textPreco.setText(String.valueOf(selecionado.getPreco()));
            textFabricante.setText(selecionado.getEsp().getFabricante());
            textCor.setText(selecionado.getEsp().getCor());
            textSistema.setText(selecionado.getEsp().getSistema());
            textDetalhe.setText(selecionado.getEsp().getDetalhe());
       }
       this.labelNome.setText(MensagensUtil.getString(MensagensUtil.MSG_LABEL_NOME));
       this.labelPreco.setText(MensagensUtil.getString(MensagensUtil.MSG_LABEL_PRECO));
       this.labelFabricante.setText(MensagensUtil.getString(MensagensUtil.MSG_LABEL_FABRICANTE));
       this.labelCor.setText(MensagensUtil.getString(MensagensUtil.MSG_LABEL_COR));
       this.labelSistema.setText(MensagensUtil.getString(MensagensUtil.MSG_LABEL_SISTEMA));
       this.labelDetalhe.setText(MensagensUtil.getString(MensagensUtil.MSG_LABEL_DETALHE));
       this.buttonSalvar.setText(MensagensUtil.getString(MensagensUtil.MSG_BUTTON_SALVAR));
       this.buttonCancelar.setText(MensagensUtil.getString(MensagensUtil.MSG_BUTTON_CANCELAR));
    }

