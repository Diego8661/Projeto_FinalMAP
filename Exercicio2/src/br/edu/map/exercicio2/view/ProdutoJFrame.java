    private void buttonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSalvarActionPerformed
        // TODO add your handling code here:
        Produto p = principal.getProduto_selecionado();
        
        if(p == null){
            p = new Produto();
            String nome = textNome.getText();
            float preco = Float.parseFloat(textPreco.getText());
            String fabricante = textFabricante.getText();
            String cor = textCor.getText();
            String sistema = textSistema.getText();
            String detalhe = textDetalhe.getText();

            p.setNome(nome);
            p.setPreco(preco);
            p.getEsp().setFabricante(fabricante);
            p.getEsp().setCor(cor);
            p.getEsp().setSistema(sistema);
            p.getEsp().setDetalhe(detalhe);

            try {
                dao.inserir(p);
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, MensagensUtil.getString(MensagensUtil.getString(MensagensUtil.MSG_ERRO)), 
                        MensagensUtil.getString(MensagensUtil.getString(MensagensUtil.MSG_ERRO_INSERIR)),JOptionPane.ERROR_MESSAGE);
            }
        }else{
            p.setNome(textNome.getText());
            p.setPreco(Float.parseFloat(textPreco.getText()));
            p.getEsp().setFabricante(textFabricante.getText());
            p.getEsp().setCor(textCor.getText());
            p.getEsp().setSistema(textSistema.getText());
            p.getEsp().setDetalhe(textDetalhe.getText());
            
            try {
                dao.editar(p);
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, MensagensUtil.getString(MensagensUtil.MSG_ERRO), 
                        MensagensUtil.getString(MensagensUtil.MSG_ERRO_EDITAR), JOptionPane.ERROR_MESSAGE);
            }           
        }
        principal.preencherTabela(null);
        dispose();
    }//GEN-LAST:event_buttonSalvarActionPerformed

